from turtle import *

#Avengers Tower
setup(600,600)
pendown()
fillcolor('slategray')
pencolor('slategray')
pendown()
begin_fill()
forward(75)
left(90)
forward(5)
left(45)
forward(120)
left(45)
forward(30)
right(90)
forward(40)
left(45)
forward(20)
left(45)
forward(20)
left(90)
forward(145)
left(90)
forward(149)
end_fill()
penup()
#Create Dot Sign
setheading(90)
forward(90)
left(100)
forward(80)
pendown()
fillcolor('blue')
pencolor('blue')
dot(110)
penup()
#Draw 'A'

setheading(0)
right(90)
forward(90)
right(90)
forward(55)
pendown()
pencolor('lightgrey')
write("A", font=("Arial Black", 120, "normal"))
penup()
forward(5)
setheading(0)
pencolor('white')
write("A", font=("Arial Black", 120, "normal"))



